#include "lftctype.h"

int	ft_isnumber(char *s)
{
	int	i;

	i = 0;
	if (s[0] == '\0')
		return (0);
	if (s[0] == '-' || s[0] == '+')
		i++;
	while (s[i])
	{
		if (ft_isdigit(s[i]) == 0)
			return (0);
		i++;
	}
	if (s[i] == '\0' && s[0] == '-' && i == 1)
		return (0);
	return (1);
}
